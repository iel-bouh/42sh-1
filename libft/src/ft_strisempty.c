#include "libft.h"

int     ft_strisempty(char *str)
{
	int     i;

	i = 0;
	if (str)
	{
		while (str[i])
		{
			if (ft_isprint(str[i]))
				return (0);
			i++;
		}
	}
	return (1);
}
